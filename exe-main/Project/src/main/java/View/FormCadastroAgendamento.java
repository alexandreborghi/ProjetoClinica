    public FormCadastroAgendamento(String id){
        this();
        this.id = id;
        
        //exibir os dados da categoria selecionada
        agendamento ca = new agendamentoDAO().pesquisarPorIdagendamento(id);
        if(ca != null){
            txtId.setText(Integer.toString(ca.getId()));
            //cbxIdEmpresa.setText(Integer.toString(ca.getId_empresa()));
            DefaultComboBoxModel m = (DefaultComboBoxModel)cbxIdPaciente.getModel();
            
            for (int i = 0; i < m.getSize(); i++) {
                agendamento agen = (agendamento)m.getElementAt(i);
                if(agen.getId() == ca.getId_paciente()){/////pegar a categoria certa
                    cbxIdPaciente.setSelectedIndex(i);
                    break;
                }
            }
            
            txtNome.setText(String.valueOf(ca.getNome()));
            txtData.setText(String.valueOf(ca.getData())); 
            txtCpf.setText(String.valueOf(ca.getCpf()));
            txtHorario.setText(String.valueOf(ca.getHorario()));
            //cbxStatus.setText(String.valueOf(ca.getStatus()));
            cbxStatus.setSelectedIndex(ca.getStatus());
        }
    }
