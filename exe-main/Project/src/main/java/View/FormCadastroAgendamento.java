    private void btnConcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConcluirActionPerformed

        agendamento obj1 = new agendamento();//recupera,retorna informações da UI
        obj1.setNome(txtNome.getText());
        //obj1.setId_empresa(Integer.parseInt(txtIdEmpresa.getText()));

        agendamento agen = (agendamento)cbxIdPaciente.getSelectedItem();
        obj1.setId_paciente(agen.getId());
        
        obj1.setData(txtData.getText());
        obj1.setCpf(txtCpf.getText());
        obj1.setHorario(txtHorario.getText());
        //obj1.setHorario((cbxHorario.getSelectedIndex()).toString());
        obj1.setStatus(cbxStatus.getSelectedIndex());

        //executar a operção de inserção
        agendamentoDAO dao1 = new agendamentoDAO();
        int resultado1;

        if(txtId.getText().isEmpty()){
            //inserir
            resultado1 = dao1.inserir(obj1);
        }else{
            //atualizar
            obj1.setId(Integer.parseInt(txtId.getText()));
            resultado1 = dao1.atualizaragendamento(obj1);
        }

        if(resultado1 == 1){
            JOptionPane.showMessageDialog(
                null,
                "Operação realizada com sucesso"
            );
        }else{
            JOptionPane.showMessageDialog(
                null,
                "Ocorreu um erro",
                "loja",
                JOptionPane.ERROR_MESSAGE
            );
        }
        this.dispose();
    }//GEN-LAST:event_btnConcluirActionPerformed

