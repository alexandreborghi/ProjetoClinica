    private void preencherComboFuncionario(){
         
        List<agendamento> lista = new agendamentoDAO().listaragendamento();
        
        if(lista != null){
            DefaultComboBoxModel m = new DefaultComboBoxModel();
            for(agendamento pro : lista){
                m.addElement(pro);
            }
            cbxIdPaciente.setModel(m);
        }
    }

